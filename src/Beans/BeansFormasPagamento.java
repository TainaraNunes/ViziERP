package Beans;

public class BeansFormasPagamento {
    int formaPagamentoCodigo;
    String formaPagamentoDescricao;

    public int getFormaPagamentoCodigo() {
        return formaPagamentoCodigo;
    }

    public void setFormaPagamentoCodigo(int formaPagamentoCodigo) {
        this.formaPagamentoCodigo = formaPagamentoCodigo;
    }

    public String getFormaPagamentoDescricao() {
        return formaPagamentoDescricao;
    }

    public void setFormaPagamentoDescricao(String formaPagamentoDescricao) {
        this.formaPagamentoDescricao = formaPagamentoDescricao;
    }       
}
